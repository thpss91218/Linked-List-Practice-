import sys


class ListNode:
    def __init__(self, data=0, pointer=None):
        self.val = data
        self.next = pointer


def ll_to_str(l_lst):
    l_str = ''
    cur = l_lst
    while cur.next is not None:
        l_str = str(cur.val) + l_str
        cur = cur.next
    l_str = str(cur.val) + l_str
    return l_str


def add_2_numbers(l1: ListNode, l2: ListNode) -> ListNode:
    l1_int = int(ll_to_str(l1))
    l2_int = int(ll_to_str(l2))
    l_sum = l1_int + l2_int
    digit_count = 0
    #
    for digit in str(l_sum):
        digit_count += 1
        if digit_count == 1:
            node = ListNode(int(digit), None)
            next = node
        else:
            node = ListNode(int(digit), next)
            next = node
    linked_list = node

    return linked_list

